     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = auth()->user();
        if ($user->type == cons('user.type.maker')) {
            $with = ['salesman', 'supplier.shopAddress', 'supplier.user.shippingAddress',];
        } else {
            $with = ['salesman', 'maker.shopAddress'];
        }
        $applyLists = $user->shop->businessRelation()->notActive()->with($with)->orderBy('status')->paginate();
        return view('index.business.trade-request-list', compact('applyLists', 'user'));
    }

